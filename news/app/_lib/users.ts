export function dateconverter(dateString: string) {
  // Split the input string by spaces to get year, month, and day parts
  const parts = dateString.split(' ')

  // Check if there are exactly three parts (year, month, day)
  if (parts.length !== 3) {
    // Log an error for invalid format and return a specific message
    console.error(
      "Error: Invalid date string format. Expected 'YYYY MM DD'. Received:",
      dateString
    )
    return 'Invalid Date Format'
  }

  // Parse parts into integers.
  // Month is 0-indexed in JavaScript Date, so subtract 1 (January is 0, December is 11).
  const year = parseInt(parts[0], 10)
  const month = parseInt(parts[1], 10) - 1 // Subtract 1 for 0-indexed month (0-11)
  const day = parseInt(parts[2], 10)

  // Create a new Date object.
  // The Date constructor handles some invalid date components by "rolling over" (e.g., Feb 30 becomes Mar 2).
  // Therefore, additional checks are needed to ensure the date is truly valid as entered.
  const date = new Date(year, month, day)

  // Validate the date:
  // 1. Check if the date object is "Invalid Date" (isNaN(date.getTime())).
  //    This catches cases where parsing failed entirely (e.g., non-numeric parts).
  // 2. Check if the year, month, and day from the created Date object match the original input.
  //    This ensures that dates like "2023 02 30" (February 30th) are correctly identified as invalid,
  //    because Date object would "correct" it to March 2nd (or similar) and the components wouldn't match.
  if (
    isNaN(date.getTime()) ||
    date.getFullYear() !== year ||
    date.getMonth() !== month ||
    date.getDate() !== day
  ) {
    console.error(
      'Error: The provided date is not a valid calendar date. Received:',
      dateString
    )
    return 'Invalid Date'
  }

  // Use toLocaleDateString to get a human-readable format.
  // 'en-US' locale ensures standard English month names.
  // 'month: 'long'' outputs full month name (e.g., "June").
  // 'day: 'numeric'' outputs day as a number (e.g., "23").
  // 'year: 'numeric'' outputs full year (e.g., "2025").
  return date.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  })
}

